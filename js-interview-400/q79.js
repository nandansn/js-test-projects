// What is isNaN

/*


The isNaN() function is used to determine whether a value is an illegal number (Not-a-Number) or not. i.e, 
This function returns true if the value equates to NaN. Otherwise it returns false.

isNaN("Hello"); //true
isNaN("100"); //false


In JavaScript NaN is short for "Not-a-Number".

The isNaN() method returns true if a value is NaN.

The isNaN() method converts the value to a number before testing it.

isNaN(undefined) // true

isNaN(null) // false

isNaN(true) // false

isNaN(false) // false

isNaN("foo") // false

isNaN(0) // false

isNaN(NaN) // true

isNaN(function() {}) // false

*/
