// What is the need of tree shaking

/*

Tree Shaking can significantly reduce the code size in any application. i.e, 
The less code we send over the wire the more performant the application will be. For example, 
if we just want to create a “Hello World” Application using SPA frameworks then it will take around a few MBs, 
but by tree shaking it can bring down the size to just a few hundred KBs. Tree shaking is implemented in Rollup and Webpack bundlers.

*/
