let users = [
  { id: "john", name: "John Smith", age: 20 },
  { id: "ann", name: "Ann Smith", age: 24 },
  { id: "pete", name: "Pete Peterson", age: 31 },
];

function groupById() {
  let newUserById = {};
  users.forEach((user) => {
    newUserById[user.id] = user;
  });

  return newUserById;
}

let usersById = groupById();

console.log(usersById);

